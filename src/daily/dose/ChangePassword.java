    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String usre_name = user.getText();
        String password_user = password.getText();

        PreparedStatement ps;
        String query = "UPDATE `users` SET `psswd`=? WHERE `userName`=?";
        try {
            ps = DbConnect.getconnection().prepareStatement(query);
            ps.setString(2, usre_name);
            ps.setString(1, password_user);
            if (ps.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(null, "Done !");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        showTableData();
    }//GEN-LAST:event_jButton1ActionPerformed

