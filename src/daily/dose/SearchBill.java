    public void getSum() {
        float sum = 0;
        for (int i = 0; i < bill.getRowCount(); i++) {
            sum = sum + Float.parseFloat(bill.getValueAt(i, 2).toString());
        }

        total.setText(Float.toString(sum));
    }

    public void showdate() {
        Calendar cal = Calendar.getInstance();
        SimpleDateFormat format = new SimpleDateFormat(DateFormat);
        searchAction.setText(format.format(cal.getTime()));
    }

    public void showTableDataI() {
        try {
            String query = "SELECT * FROM `bill`";
            ps = DbConnect.getconnection().prepareStatement(query);
            rs = ps.executeQuery();
            bill.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

    ////////Date Select
    public void dateSelect() {

        String search = searchAction.getText();
        String query = "SELECT `billNum`, `date`, `total` FROM `bill` WHERE `date`=?";

        try {
            ps = DbConnect.getconnection().prepareStatement(query);

            ps.setString(1, search);
            rs = ps.executeQuery();
            bill.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);

        }

    }

    //Select Between two Dates
    public void betweenTwoDates() {

        String srtat_date = startDate.getText();
        String end_date = endDate.getText();

        String query = "SELECT `billNum`, `date`, `total` FROM `bill` WHERE `date` BETWEEN ? AND ?;";

        try {
            ps = DbConnect.getconnection().prepareStatement(query);

            ps.setString(1, srtat_date);
            ps.setString(2, end_date);
            rs = ps.executeQuery();
            bill.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);

        }

    }

